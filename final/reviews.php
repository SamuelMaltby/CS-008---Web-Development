            <h2><em>KOD</em> - J. Cole</h2>
            <h4 class="credit">-- Pitchfork Scores it <span class="score">6.3/10</span></h4>
            <h3>With his fifth album, the North Carolina rapper aims for
                righteousness but often ends up sounding self-righteous instead.</h3>
            <p class="first">Listening to a J. Cole album can feel like listening to
                a very intense young lawyer attempt to win a difficult case. Throughout
                his career, Cole’s raps have often been self-serious and polemical, with
                their success depending on the overall strength of his argumentation above
                all else. And while many of his individual claims can be convincing, you
                often get to the end of a song and think something like: Wait, did he really
                just argue that corporations take taxes and use them to buy and spread guns?
                Few artists stake so much on their ability to persuade an audience of their
                worldview, particularly when that worldview is so absolutist. You do not listen
                to J. Cole to enjoy his wit or his stories, but to partake in his wisdom, which
                often involves an element of moral panic: On his new addiction-themed album, KOD,
                he loves to suggest that people should abstain from things—smoking, drinking, online
                dating. Sometimes, he’s persuasive, but just as often, he simply seems self-righteous.</p>

            <p class="first">For a talented technical rapper with reverence for hip-hop’s history,
                Cole has never really been playful. (His previous album, 4 Your Eyez Only, was
                all about death.) Aside from his weakness for corny punchlines, his verses are
                frequently free of the word games that his top-tier peers revel in. But even so,
                Cole is capable of making a strong case for his beliefs. When he does, it’s thanks
                to the emotional appeals he embeds in certain songs. On “FRIENDS,” he confesses to
                his dependence on weed before calling out specific friends who abuse drugs; in asking
                them to stop, he mostly ditches his sanctimony. On another standout, “Kevin’s Heart,”
                Cole uses the pint-sized comedian’s very public infidelities to reflect on the challenge
                of monogamy: “My phone be blowing up/Temptations on my line/I stare at the screen a
                while before I press decline.” Cole is most effective when he keeps things personal
                rather than turning up his nose at the choices of others.</p>

            <p class="first">Other songs work because of the North Carolina rapper’s
                technical ability and skill behind the boards. Previously, when Cole
                has wanted to make a statement, he’s asked all collaborators to leave
                the room. The new album, like his would-be magnum opus, 2014 Forest Hills
                Drive, is absent of other artists (save kiLL edward, a mysterious guest whose
                voice, when sped up, sounds like J. Cole’s), and Cole produced much of it
                himself. “ATM” and the title track are potent reminders of the way he can
                rip up a song with his flow alone. Cole is friends with Kendrick Lamar,
                and KOD, with its stripped-down production, snare-drum flows, and focus on
                virtue and vice, can feel like a pale shadow of DAMN. Unlike the Pulitzer
                winner, Cole is far more predictable and accessible.</p>

            <p class="first">Cole simplifies lust on “Photograph,” where he again reminds
                us that his ideal woman is a holy Madonna who is sexy but never shows too
                much skin. He’s thoughtlessly proud on “The Cut Off,” in which he commends
                himself for his generosity after talking about how tempted he is to become
                violent. And on “BRACKETS,” he boils down a complicated topic in order to
                make a difficult argument work. The song ends with the story of a mother who
                loses her son. On the day of her son’s funeral, she remembers she has to pay
                her taxes, which she believes indirectly funded his death. Much like DAMN., the
                song uses the cyclical nature of time to make a point—that taxes are evil. But
                unlike Kendrick, Cole jury-rigs the narrative to make his case. Specious as they
                may be, songs like “BRACKETS” help us understand Cole’s appeal: He unites his
                audience against bogeymen like taxes, or the government, or white teachers in
                black schools, or a new generation of rappers. If you agree that those things
                are unequivocally Bad, you might be willing to listen when he tells you what not to do.</p>

            <p class="first">The most personal song on KOD is about his mother, Kay,
                and it combines the emotional appeal and evocative detail of the album’s
                best tracks with the selfishness of its worst. On “Once an Addict (Interlude),”
                Cole recalls his mom drinking too much after his stepfather had a child by
                another woman—even pinpointing the Marvin Gaye and Al Green songs she was
                listening to at the time—as he regrets his own callous reaction (“Why she
                always using me for a crutch?”). But while those elements make the song powerful,
                Cole expresses a surprising lack of empathy for his mother’s troubles, even with
                the benefit of hindsight, and is hamstrung by clumsy lines (“Maybe things get better
                with time, I heard it heals”). Listeners are asked to think about the rapper’s pain,
                rather than his mother’s.</p>

            <p class="first">Kay has encouraged her younger son to use her struggles
                as a warning to others, which he has done in the past. But there’s more
                to her story. She worked hard raising two sons by herself, working as a
                mail carrier to the point where her feet ached. One of those sons became a
                successful rapper, bought her a house, granted her the opportunity to stop
                carrying mail and to pursue acting, to meet Barack Obama and Afeni Shakur.
                In its haste to show the ugliness of her addiction KOD’s portrait of her
                glosses over any and all explanatory or redemptive details. It misses many
                other things too, when it conflates healthy and abusive self-medication, or
                refuses to make a distinction between marijuana, alcohol, and harder drugs.
                Because Cole is so often trying to convince us, he frequently only tells one
                side of the story, concealing the full picture in order to win an argument
                that nobody started.</p>

            <cite>-- Reviewed by <a href="https://pitchfork.com/reviews/albums/j-cole-kod/"><i class="score">Pitchfork</i></a> --</cite>

            <h2><em>True Sadness</em> - The Avett Brothers</h2>
            <h4 class="credit">-- Pitchfork Scores it <span class="score">3.8/10</span></h4>
            <h3>It’s fitting that the Avett Brothers’ most disappointing record yet bears the title of
                True Sadness. Is this some kind of play for the Top 40, or just instrumental bloat and trite sentiment?</h3>

            <p class="first">The Avett Brothers once seemed to hold an infinite amount of promise. They were a
                hardworking group of young men who traded in songs that were as poignant as they were ragged.
                The titular Avetts, the older Scott and younger Seth, wore their bleeding hearts on their sleeves,
                and they always came off as earnest, honest, regular fellas from North Carolina who were
                thoughtfully dedicated to their craft.</p>

            <p class="first">The band’s tide started to turn toward the big time in 2007 with the sparkling
                Emotionalism, and the subsequent I and Love and You found the band working with the famed
                Rick Rubin as their producer. But the band’s work with Rubin has yielded records scrubbed
                of the rawness and grit that made the band so compelling in its early years. It’s fitting,
                then, that The Avett Brothers’ most abysmal and disappointing record yet bears the title of
                True Sadness.</p>

            <p class="first">True Sadness is a record that can’t seem to get out of its own way. Almost every
                track is bloated with instrumentation. Several songs are smothered with baffling layers of
                synthesizers in what feels like a ploy to push the Avetts into clear Top 40 territory.
                They were never a bluegrass band, of course, and always existed in the interesting overlapping
                gray areas between folk, Americana, and rock circles. But the synths and electronic beats that
                burble up on “You Are Mine” and “Satan Pulls the Strings” don’t make sense for the band in any
                context. The shift isn’t even bold enough to warrant a “Dylan goes electric” comparison. Instead,
                it sounds like a half-baked bad idea that no one had the guts or sense to veto. The slathered-on
                saccharine arrangements sink to their most unfortunate low on closing track “May It Last,” which
                aims for elegance and intensity but instead comes through as a ham-fisted and embarrassing attempt
                at something serious.</p>

            <p class="first">Somewhere along the way, the Avetts lost a grip on their lyrical skills, too. True
                Sadness presents some astonishingly bad turns of phrase, as when Seth Avett sings about wishing
                to be “a tune you sang in your kitchen,” so that he “could float around your tongue and ease the
                tension.” Elsewhere, songs manage to be preachy and completely self-unaware, as the Avetts sing
                about being tempted by Satan, struggling with belief, and being victimized by the grand scheme of
                life itself. Losing love was once an occasion for collapsin’ and screamin’ at the moon, but the
                Avetts got all of that drama out of their system in 2008, it seems. Now, they offer the likes of
                the self-explanatory “Divorce Separation Blues,” which feels like a dopey “Oops, my bad” at
                best—goofy yodels don’t much help the song’s case for sympathy, either. Scott and Seth Avett sing
                about guilt and feeling bad, but these songs ring hollow. It’s as if the Avetts knew folks might
                expect heartfelt confessional material—especially in the wake of Seth’s slightly scandalous split
                from his wife—and wrote a clutch of songs that half-assedly check the box.</p>

            <p class="first">The band does find itself making one short breakthrough with “Fisher Road to
                Hollywood,” an intimate track that feels like being snapped from a bad dream. The song,
                buried on the back half of the record, is almost a sincere mea culpa, an admission of
                regret for the way that things went seriously wrong for the band as a unit and as individuals.
                The band returns to its early strong suit: Gentle acoustic strums, mournful cello, plaintive
                vocals. There’s the sense that all hope isn’t lost for the Avett Brothers, that maybe they
                could scrap all of the messy baggage they’ve acquired in their rise from grimy clubs to sold-out
                arenas and major label dealings. But then, those four-and-a-half minutes are over, and you’re
                forced to reckon with the empty misery of True Sadness once again.</p>


            <h2><em>Mothership</em> - Led Zeppelin</h2>
            <h4 class="credit">-- Pitchfork Scores it <span class="score">7/10</span></h4>
            <h3>Essentially a primer for those who actually might not be familiar with the Led Zeppelin catalog,
                this is a collection of unimpeachable songs in search of an audience.</h3>
            <p class="first">Led Zeppelin were never much of a singles band: From 1969 on, guitarist Jimmy
                Page was adamant that the group's LPs not be mucked up with filler, so most of
                their releases contain only between eight and 10 tracks. Led Zeppelin's legacy
                so significantly shaped the next four decades of rock'n'roll and heavy metal
                that to further discount any one of those tracks seemed brash, negligent, or stupid.
                Twelve years of intense self-editing meant that Led Zeppelin required no best-of.
                Anyone truly interested in their music was advised to dig through their couch cushions
                until they came up with enough quarters to invest in one (or all) of the band's albums:
                It would be worth every penny.</p>

            <p class="first"><em>Mothership</em>-- a collection of 24 of Led Zeppelin's "greatest hits," remastered
                and selected by the band's three remaining members (Page, as well as Robert Plant
                and John Paul Jones)-- might be conceptually flawed, but it's hard to argue against
                songs as indisputably great as these. <em>Mothership</em> is not the first time Page has participated
                in the distillation of his own work: In 1990, a decade after John Bonham downed his last shot
                of vodka, Page curated an eponymous boxset (known, in fan parlance, as Crop Circles) which saw
                their eight-album discography distilled to four perfect CDs. And in 2002, Atlantic released Early
                Days and Latter Days: Volumes 1 and 2 (each previously available as a single purchase, in 1999 and
                2000, respectively), a Page-condoned collection of 23 tracks which awesomely featured all four members
                of Led Zeppelin on the cover, dressed in giant American space suits and perched casually in front of an
                illustrated moonscape. Early Days and Latter Days has a tracklist similar to <em>Mothership</em> (the latter
                adds "D'yer Mak'er", "Over the Hills and Far Away", "Heartbreaker", and "Ramble On", but loses
                "Ten Years Gone", "What Is and What Should Never Be", and "The Battle of Evermore") so, clearly,
                the idea of Led Zeppelin's greatest hits is contentious even for the band itself.

            <p class="first">Fans will probably argue over Led Zeppelin's selections-- omitting "What Is and What Should Never Be"
                feels unforgivable-- and it seems obvious that Page, Plant, and Jones were striving hard to represent
                every last phase of their career: Some work from the band's crucial 1969-1975 era is sacrificed for
                late-career tokens like the comparably-mediocre "In The Evening" (itself a curious selection, given
                the relative popularity of In Through the Out Door's "Fool in the Rain", which was the band's last
                release to make the pop single charts). Still, content-critiquing aside, the remastering on <em>Mothership</em>
                is no small achievement, and it sounds revelatory on even the shittiest stereos. Check Bonham's
                pitter-pattering in the verses of "Ramble On", or the still-intact fuzz at the top of "Immigrant Song",
                or the tiny percussive "fwwpts" on "D'yer Mak'er", or the restored outro to "Over the Hills and Far Away".
                Twist your volume knob heavenward and embrace Plant's inescapable geekiness, yelping about Gollum and Mordor;
                chug booze and air-drum with John Bonham: Led Zeppelin sound as tremendous in 2007 as they did in 1972.

            <p class="first"><em>Mothership</em> offers no bonus content or previously unreleased material. It serves, mainly, as an
                introduction for those (presumably out of classic rock radio range) still unfamiliar with the band.
                Longtime fans will find little of use here, although anyone who spends a couple hours with <em>Mothership</em>
                will find themselves duly reminded that Led Zeppelin are quite possibly the finest rock band of all time.
            </p>
            <cite>-- Reviewed by <a href="https://pitchfork.com/reviews/albums/10890-mothership/"><i class="score">Pitchfork</i></a> --</cite>

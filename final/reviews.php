            <h2><em>Wincing the Night Away</em> - The Shins</h2>
            <h4 class="credit">-- Pitchfork Scores it <span class="score">7/10</span></h4>
            <h3>James Mercer & co. aim to deflect the inevitable Brafflash by crafting a more
                texturally diverse record than their outstanding, if uncomplicated, previous outings.</h3>

            <p class="first">While indie rock has embraced grander and more elaborate productions,
                the Shins have remained unlikely champions of uncertainty and understatement. Unlike
                many of their meteorically successful indie peers, the Shins don't want to change your
                life-- and that's a good thing, because the band's biggest strength is an uncanny gift
                for conjuring a deep, vivid, and palpable sense of the familiar. Many of the Shins' best
                songs evoke a feeling of comfort and closeness that's immediately recognizable but rarely
                experienced-- intimacy is the band's best weapon, amplifying the subtle ebbs and flows of
                their music so that the slightest injection of unease or melancholy hits with remarkable force.</p>

            <p class="first">On their third Sub Pop full-length, Wincing the Night Away, the Shins take a
                decisive but wobbly step out of their comfort zone, and in doing so sacrifice much of this
                musical/emotional proximity effect. While the band has taken a good deal of criticism for
                sounding "too average" or "boring," the ill-suited sonic punch of Wincing the Night Away
                throws the singular strengths of their previous work into stark relief. The almost-live
                sounding Chutes Too Narrow left plenty of room for singer James Mercer's excellent vocals
                to guide its songs both melodically and rhythmically. But on Wincing, too-loud drums and
                bass distract not only from the elegant movement of Mercer's melodies, but from the delicate
                harmonic tensions that underlie them.</p>

            <p class="first">That said, the first four tracks of Wincing are unerringly solid. Echoing the
                striking contrast of Chutes Too Narrow opener "Kissing the Lipless", Wincing's lead track,
                "Sleeping Lessons", builds from sparse, muffled arpeggios to full-on rock'n'roll. While slick
                and robust production doesn't flatter much of the record, it works well as a counterpoint
                to the song's quiet initial moments. "Australia" is a peppy rocker in the spirit of Chutes'
                best, elevated by a newfound confidence and expressive range in Mercer's voice. Single
                "Phantom Limb" is pure, lush pop, boasting a chorus that plays like the aural equivalent
                of that optical illusion where a staircase appears to ascend indefinitely.</p>

            <p class="first">With the exception of the excellent "Red Rabbits", the more noticeable aesthetic
                departures on Wincing don't fare as well. "Sea Legs", with its intrusive synthesized drum beat
                and lackluster arrangement, brings to mind that unfortunate Eve 6 song about putting your heart
                in a blender, while "Spilt Needles" comes off as sterile and overcalculated, despite its strong
                chorus. Still, the album finishes strong: "Girl Sailor" surpasses "Phantom Limb" in lyrics and
                overall structure, making it a likely contender for the album's second single, and "A Comet
                Appears" is beautifully orchestrated, if not terribly memorable, making it an appropriate
                closer for a record that often emphasizes texture over form.</p>

            <p class="first">There's a time-honored imperative to encourage bands for attempting to develop
                and expand, and the Shins could certainly take their music in many different directions with
                great success. But it's hard not to notice that the least adventurous tracks on Wincing the
                Night Away are generally the most rewarding. In many cases, the album's more experimental
                touches seem at odds with the natural elegance of Mercer's songwriting, making it hard to
                read the album as a shoddy blueprint of what a more "difficult" Shins record might sound
                like. Instead, Wincing the Night Away is a lovely and well-executed album and-- for the
                first time in the band's career-- nothing more.</p>


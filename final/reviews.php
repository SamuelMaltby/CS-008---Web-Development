            <h2><em>Igor</em> - Tyler, the Creator</h2>
            <h4 class="credit">-- Pitchfork Scores it <span class="score">8/10</span></h4>
            <h3>Tyler, the Creator’s sixth album is impressionistic and emotionally charged,
                the result of an auteur refining his style and baring more of his soul than ever before.</h3>

            <p class="first">The moods of Tyler, the Creator’s albums have largely been defined by
                absence—of his father, of critical acclaim, of love. He responded to what was missing
                with antagonism, album after album, until 2017 when he looked back at his life with a
                sunny lens and twinge of nostalgia to deliver his best work, Flower Boy. That Grammy-nominated
                album is eminently pleasing, the sound of an iconoclast succumbing to his better judgment. IGOR,
                the 28-year-old’s sixth full-length, is Tyler finally content in the face of all that agony.</p>

            <p class="first">IGOR sounds like the work of a perfectionist giving shape to his more radical ideas.
                Tyler, who proudly produced, wrote, and arranged the album, is singing more but he’s not worrying
                whether his tracks have a traditional pop arc. Songs don’t build to a crescendo, they often begin
                there. The opening “IGOR’S THEME” serves less as a guiding force and more like a recurring motif
                of doom that hides in the shadows and pops its head in at select moments, like on “NEW MAGIC WAND”
                where spooky synths erupt below Tyler’s thought process: “I saw a photo, you looked joyous,” goes
                one of the more poignant lines. Atop this budding dread, Tyler layers candied keys and harmonizing
                vocals. The brightness is defiant, as Tyler processes the loss of someone he loves.</p>

            <p class="first">The first we hear of Tyler’s vanishing relationship is on “EARFQUAKE”: “Don’t leave,
                it’s my fault.” First pitched-up and later untreated, Tyler’s voice is pleading but not cloying.
                He doesn’t sound like he’s lying to quickly repair deep damage, as his words may suggest, he’s
                just being sincere. IGOR becomes a gracious and giving breakup album whose narrative is fleshed
                out more clearly later in the record: Tyler seems to have fallen for a man (“You’re my favorite
                garçon,” he sings at one point) who wants to return to his female partner. “I hope you know she
                can’t compete with me,” he first sings on “GONE, GONE / THANK YOU,” before shifting his tone:
                “Thank you for the love/Thank you for the joy.”</p>

            <p class="first">As the album progresses, Tyler goes through his undulations of denial and acceptance,
                but spends considerable energy hoping to help his beloved find satisfaction, even if that means a
                future without him. “Take your mask off,” he advises on “RUNNING OUT OF TIME,” “Stop lyin’ to
                yourself, I know the real you.” It’s an empathetic turn from an artist previously allergic to
                other people’s perspectives. The parting ultimately leads to self-discovery: “You never lived
                in your truth,” he tells his ex. “But I finally found peace, so peace.”</p>

            <p class="first">There’s a run at IGOR’s center where each song’s momentum seems to propel him
                forward emotionally. It’s during this stretch that Tyler is at his most creatively fluid, as
                on “A BOY IS A GUN*,” where he flattens his voice to sing “gun,” sounding like a laser cutting
                across the track and maybe also through his own psyche. Combined with the Kanye-assisted
                “PUPPET,” these tracks in their varied tone and tempo reflect the volatility of Tyler’s
                emotions across IGOR. Most songs don’t even have a natural ending, they just snap off,
                like someone pulled the aux cord abruptly.</p>

            <p class="first">IGOR may be unsettled but it never feels restless. As Tyler grapples with
                uncertainty and unfulfillment, he delivers an album that feels like it is suspended in midair.
                It reminds me of Solange’s When I Get Home or King Krule’s The OOZ, albums that succeed in
                communicating mood as their own sense of logic. Tyler’s interpretation of this sort of
                stream-of-consciousness feels weightless. The whole album is sustained by mutating,
                colorful chords, impressionistic cracks in tonality. On top of that, Tyler’s synthetic
                falsetto singing adds a surreal element to IGOR. The lines between desire and reality
                and internal monologue and human conversation all become blurred.</p>

            <p class="first">Tyler, the Creator never shied away from sharing what he thought his life
                was missing. “I ain’t got no fucking money,” he yelled simply enough on the inimitable
                “Radicals,” an early Odd Future anthem. And when he got what he thought he wanted, he
                flaunted it: “Also stuck with a beautiful home with a case of stairs,” he taunted his
                father on “Answer.” IGOR is the first time Tyler has not been motivated by some absence
                because he lost a bit of himself in someone else. “ARE WE STILL FRIENDS?,” the album’s
                rough and honeyed send-off, is Tyler’s final attempt at salvaging his relationship. He’s
                finally without his beau and asks for the compromise of friendship. The track, as with
                many on IGOR, ends sharply with a synth never resolving its buzz. There’s nothing left to
                say when you’ve given all of yourself away.</p>

            <cite>-- Reviewed by <a href="https://pitchfork.com/reviews/albums/tyler-the-creator-igor/"><i class="score">Pitchfork</i></a> --</cite>

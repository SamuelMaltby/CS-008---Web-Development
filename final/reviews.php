            <h2><em>Dicaprio 2</em> - J.I.D</h2>
            <h4 class="credit">-- Pitchfork Scores it <span class="score">7.7/10</span></h4>
            <h3>The East Atlanta rapper’s second album doesn't try to take on the world and is all the
                better for it, showing that J.I.D. has much more to offer than his formidable rapping chops.</h3>

            <p class="first">Four tracks into DiCaprio 2, J. Cole, shows up for a feature. Typically,
                on these sort of master-student collaborations, the less-experienced rapper’s strain is
                audible as he attempts to show he can hang. Here, it’s the opposite: J.I.D’s opening verse
                is so gleeful, so effortless and so fast, that when it comes Cole’s turn to rap, you can
                almost hear him brace for impact. Cole gets off a strong verse, and the song, “Off Deez,”
                is one of many standouts here. But you can just imagine them stepping out of booth, J.I.D
                pumped and ready for more; Cole relieved that he can still rap as well as his younger signee.</p>

            <p class="first">J.I.D’s confidence comes from years of work. Finally starting to get his due
                at the age of 28, Destin Route built up a head of steam in the old-fashioned way (circa 2007,
                or B.I.G., Before Instagram), releasing mixtape after mixtape until he turned heads with the
                first DiCaprio project, an EP released at the top of 2015. He signed with Cole’s label,
                Dreamville, in 2017 and his debut album, The Never Story, was released in March and converted
                rap fans all year mostly off the strength of the rapper’s technical ability and the delight
                that he takes in displaying it. That ability has been refined further on DiCaprio 2, but
                J.I.D’s second album has more to offer than simple rapping chops.</p>

            <p class="first">Let’s talk about those for a second though. J.I.D’s hyper-enunciated,
                high-pitched whine of a flow is initially reminiscent of Kendrick Lamar’s, but that
                comparison feels narrow. (He plays with the connection explicitly on “Despacito Too,”
                interpolating Kendrick lyrics in a narrative about his own individual development.)
                J.I.D’s melodic instincts are strong, sharing ties with Isaiah Rashad and even,
                occasionally, Bone Thugs, and he has the ability to get jokes off even in the midst
                of triple-time rapping, a la Big L or Lil Wayne. Though he can rap faster than almost
                any newcomer in recent memory, he uses the speed as one of several tools and his flow is
                chameleonic, suited to the given moment. (His pen name comes from his grandma’s chosen
                adjective for him, “jittery,” and it’s the right word for this restless rapper.)</p>

            <p class="first">And then, of course, there are little lyrical gems. The best lines here
                feel like J.I.D is performing acrobatics on a 20-foot tightrope; many of his lyrics
                operate on the basis of his speed jousting with listener's expectations and making his
                songs surprising and relistenable. A bizarre line zips by in “Westbrook,” an A$AP Ferg
                feature, “Live life like a baby that was dead at birth but came alive and fucked the
                nurses,” the deadpan absurdity hitting your ears so quickly that you want to hear it
                again, just to confirm that he really said it. He also makes great use of little
                switchbacks; on “Off Da Zoinkys,” an anti-drug anthem, he thanks Ronald Reagan
                (ostensibly for the War on Drugs) and as you’re gearing up to react, he immediately
                takes it back, calling the 40th president “racist as fuck.”</p>

            <p class="first">“Off Da Zoinky’s” gives a window into J.I.D’s appeal. Given that it’s an
                anti-drug sermon of sorts, it could easily have been sanctimonious. Instead, it’s
                self-aware (“I ain’t nosy but I know what I know, Mr. Know-It-All, oh here he go”)
                and it builds over time. J.I.D. eventually says that the reason he stays drug-free
                is that he wants his mind clear and his focus sharp because he’s trying to be the
                greatest. That higher purpose gives the song more in common with Andre 3000 on
                “ATLiens” (”No drugs or alcohol so I can get the signal clear as day”) than with
                J. Cole’s album-long campaign against sin, KOD.</p>

            <p class="first">DiCaprio 2’s lyrical showcase takes a turn on “Workin’ Out,” and
                across the remainder of the album, J.I.D tries different things, usually successfully.
                (The clear exception is “Skrawberries,” a thick-headed treatise on gender relations that
                wastes a Mac Miller beat and comes complete with the line “[she] got a couple abortions,
                now that pussy’s a haunted house.”) “Tiiied,” a song about relationship drama, opens on
                J.I.D fighting with his girl at the movies sounding a little like Anderson .Paak with a
                fine eye for detail. The song is produced by Dreamville’s Elite and Ron Gilmore and it’s
                filled with jazzy musical flourishes that, like many of the beats here, catch your ear
                anew once you’ve become familiar with the verses. But the strongest song on the album’s
                back half is “Hot Box,” as J.I.D works out his New York fetishism with a head-turning
                guest verse from Method Man and some lovely neo-boom bap courtesy of Skhye Hutch
                (who’s produced for Ab-Soul and Kendrick) and Zorro.</p>

            <p class="first">J.I.D’s presence on Dreamville makes all kinds of sense. He shares
                Cole’s values and the priorities and pleasures of his music are those of an older
                generation. On the late-album standout “Just Da Other Day,” he says “I’m tryna get
                my fuckin goals, not a goyard,” and his anti-materialist outlook and low-key contempt
                for his contemporaries tracks perfectly with his label boss. But nothing about J.I.D
                is crochety or bitter. His songs are about joy and hunger and reflection and fun. Not
                one of them feels as if it’s trying to save hip-hop.</p>

            <cite>-- Reviewed by <a href="https://pitchfork.com/reviews/albums/jid-dicaprio-2/"><i class="score">Pitchfork</i></a> --</cite>

            <h2><em>My Beautiful Dark Twisted Fantasy</em> - Kanye West</h2>
            <h4 class="credit">-- Pitchfork Scores it <span class="score">10/10</span></h4>
            <h3>Kanye's big year culminates in an LP that feels
                like an instant greatest hits, the ultimate realization
                of his strongest talents and divisive public persona.</h3>
            <p class="first">Kanye West's 35-minute super-video, Runaway, peaks with a parade.
                Fireworks flash while red hoods march through a field. At the center of the
                spectacle is a huge, pale, cartoonish rendering of Michael Jackson's head.
                My Beautiful Dark Twisted Fantasy's gargantuan "All of the Lights" soundtracks
                the procession, with Kanye pleading, "Something wrong, I hold my head/ MJ gone,
                our nigga dead." The tribute marks another chapter in West's ongoing obsession
                with the King of Pop.</p>

            <p class="first">West's discography contains innumerable references and allusions
                to Jackson. His first hit as a producer, Jay-Z's "Izzo (H.O.V.A.)", sampled the
                Jackson 5's "I Want You Back". For many, his first memorable lines as a rapper
                came during 2003's "Slow Jamz": "She got a light-skinned friend look like Michael
                Jackson/ Got a dark-skinned friend look like Michael Jackson." And when West's
                recent interview with Matt Lauer on the "Today" show went awry, he took to Twitter,
                writing, "I wish Michael Jackson had twitter!!!!!! Maybe Mike could have explained
                how the media tried to set him up!!! It's all a fucking set up!!!!" Like most everything
                else, Kanye may exaggerate the kinship, but it's real. And it's never more apparent than
                on Twisted Fantasy, a blast of surreal pop excess that few artists are capable of
                creating, or even willing to attempt.</p>

            <p class="first">To be clear, Kanye West is not Michael Jackson. As he told MTV last
                month, "I do have a goal in this lifetime to be the greatest artist of all time,
                [but] that's very difficult being that I can't dance or sing." He ended the thought
                with a laugh, but you get the impression he's not kidding. Unlike Michael, he's not
                interested in scrubbing away bits of himself-- his blackness, his candidness-- to
                appease the masses. And while Jackson's own twisted fantasies of paranoia and betrayal
                eventually consumed him whole, West is still aware of his illusions, though that
                mindfulness becomes increasingly unmoored with each newspaper-splashing controversy.
                The balance is tenuous, but right now it's working to his advantage. On Twisted
                Fantasy, Kanye is crazy enough to truly believe he's the greatest out there. And,
                about a decade into his career, the hardworking perfectionist has gained the talent
                on the mic and in the control room to make a startlingly strong case for just that.</p>

            <p class="first">Kanye's last album, 2008's 808s and Heartbreak, was heavy on the
                Auto-Tune and stark synths, but relatively light on grandiose ideas. It was a
                necessary detour that expanded his emotional palette; a bloodletting after a
                harsh breakup and the passing of his mother that manifests itself in Twisted
                Fantasy's harshest lows. But musically, the new album largely continues where
                2007's Graduation left off in its maximalist hip-hop bent, with flashes of The
                College Dropout's comfort-food sampling and Late Registration's baroque
                instrumentation weaved in seamlessly. As a result, the record comes off like a
                culmination and an instant greatest hits, the ultimate realization of his strongest
                talents and divisive public persona. And since the nerd-superstar rap archetype he
                popularized has now become commonplace, he leaves it in the dust, taking his style
                and drama to previously uncharted locales, far away from typical civilization.</p>

            <p class="first">He's got a lot on his mind, too. After exiling himself for months
                following last year's infamous Taylor Swift stage bomb, the rapper made some of
                his first comeback appearances at the headquarters of Facebook and Twitter in late
                July. Videos of West standing on a table in tailored GQ duds while gesticulating
                through new rhymes (sans musical accompaniment) quickly made the rounds. The Silicon
                Valley visits seemed like a stunt, but they were prophetic. Forever an over-sharer,
                Kanye was looking for an outlet for his latest mirror-born musings. He found that
                platform with Twitter, and proceeded to dictate his own narrative in 140 character
                hits. Whether showing off exotic purchases, defending himself against the press, or
                going on stream-of-consciousness rants, Kanye finally had the middleman-free,
                instant-gratification platform he'd always wanted.</p>

            <p class="first">Juiced on the direct connection, he began releasing weekly songs for
                free online, the generosity of which would be moot if the songs didn't deliver.
                But they did, over and over, eventually building up the same type of superstar
                goodwill Radiohead pulled off with their pay-what-you-want In Rainbows release
                plan and Lil Wayne's free mixtape barrage leading up to 2008's Tha Carter III.
                So while Kanye can't sing or dance like Michael, he's making meaningful connections
                in a fresh, oftentimes (ahem) naked way. "When I used to finish an album I would be
                so excited for my mom to hear the final - final!" he wrote on November 11. "The
                final - final is what we used to call the... completed album with all the skits!!!
                I made songs to please one person... MY MOM!!! I would think... would my my Mom like this song!"</p>

            <p class="first">I'm not sure which song he's talking about. Because, between July and
                November, West seemingly decided to make My Beautiful Dark Twisted Fantasy less
                mom-friendly and more of a hedonistic exploration into a rich and famous American
                id. At Facebook, he rapped the first verse of what would become album closer "Lost
                in the World", at one point changing Michael Jackson's "Wanna Be Startin' Something"
                refrain to, "Mama-say mama-sah Mama Donda's son," referring to his late mother. The
                familial allusion was left off the album. Another Facebook tune-- a brutally oedipal
                account called "Mama's Boyfriend"-- was also deleted, along with the
                vintage-Kanye-sounding "See Me Now". Such exclusions speak to the album's sharp
                focus-- to move everything forward while constantly tipping on the brink of frantic instability.</p>

            <p class="first">This isn't the same resourceful prodigy who made The College Dropout or even the
                wounded soul behind 808s and Heartbreak. Instead, Kanye's Twisted Fantasy incarnation cherry-picks
                little things from his previous work and blows them up into something less than sane. The expansive,
                all-encompassing nature of the album is borne out in its staggering guest list which includes mentors
                Jay-Z, RZA, and No ID, along with new charges like Nicki Minaj, Rick Ross, Kid Cudi, and Bon Iver's
                Justin Vernon. The inclusion of Minaj (who contributes the schizoid verse of her life on "Monster"),
                Ross (a guy known for making up his own reality as he goes), and Cudi (who's probably even more wildly
                self-destructive than Kanye) especially adds to the hallucinatory tone. By the time Chris Rock shows
                up to provide comic relief during one of the album's bleakest moments, it begins to feel as if Kanye
                is stage-managing his own award show with enough starpower, shock, and dynamism to flatten the Grammys,
                the VMAs, and the rest all at once.</p>

            <p class="first">Over the past few months, Kanye has intermittently tried to flush away his rep as a
                boorish egoist in interviews and on Twitter, which is, fortunately, impossible. Because without his
                exploding self-worth-- itself a cyclical reaction to the self-doubt so much of his music explores--
                there would be no Twisted Fantasy. "Every superhero needs his theme music," he says on "POWER", and
                though he's far from the virtuous paragons of comic book lore, he's no less complex. In his public
                life, he exhibits vulnerability and invincibility in equal measure, but he's just as apt at
                villainy-- especially here.</p>

            <p class="first">With "Runaway", he rousingly highlights his own douchebaggery, turning it into a
                rallying cry for all humanity. Like many of his greatest songs, it's funny, sad, and perversely
                relatable. And while the royal horns and martial drums of "All of the Lights" make it sound like
                the ideal outlet for the most over-the-top boasts imaginable, West instead inhabits the role of an
                abusive deadbeat desperate to make good on a million blown promises. "Hell of a Life" attempts to
                bend its central credo-- "no more drugs for me, pussy and religion is all I need"-- into a noble
                pursuit. As a woofer-mulching synth line lurks, Kanye justifies his dreams of not sleeping with
                but marrying a porn star, peaking with the combative taunt, "How can you say they live they
                life wrong/ When you never fuck with the lights on." Inspired by his two-year relationship
                with salacious model Amber Rose, the song blurs the line between fantasy and reality, sex and
                romance, love and religion, until no lines exist at all. It's a zonked nirvana with demons
                underneath; a fragile state that can't help but break apart on the very next song.</p>

            <p class="first">The haunted, Aphex Twin-sampling "Blame Game" bottoms out with a verse in
                which Kanye's voice is sped up, slowed down and stretched out. The effect is almost psychotic,
                suggesting three or four inner monologues fighting over smashed emotions. It's one of many
                moments on the record where West manipulates his vocals. Whether funneling some of his
                best-ever rhymes through a tinny, Strokes-like filter on "Gorgeous" or making himself
                wail like a dying cyborg in the final minutes of "Runaway", he uses studio wizardry to
                draw out his multitudes. Tellingly, though, he doesn't get the last word on the album.
                That distinction goes to the sobering tones of Gil Scott-Heron's 1970 spoken-word piece
                "Comment #1", a stark take on the American fable. "All I want is a good home and a wife
                and children and some food to feed them every night," says Scott-Heron, bringing the fantasy to a close.</p>

            <p class="first">On "POWER", Kanye raps, "My childlike creativity, purity, and honesty is
                honestly being crowded by these grown thoughts/ Reality is catching up with me, taking
                my inner child, I'm fighting for custody." The lines nail another commonality between
                the rapper and his hero. Like Michael, Kanye's behavior-- from the poorly planned outbursts
                to the musical brilliance-- is wide-eyed in a way that most 33 year olds have long left
                behind. That naivety is routinely battered on Twisted Fantasy, yet it survives, better
                for the wear. With his music and persona both marked by a flawed honesty, Kanye's man-myth
                dichotomy is at once modern and truly classic. "I can't be everybody's hero and villain,
                savior and sinner, Christian and anti Christ!" he wrote earlier this month. That may be true,
                but he's more willing than anyone else to try.</p>

            <cite>-- Reviewed by <a href="https://pitchfork.com/reviews/albums/14880-my-beautiful-dark-twisted-fantasy/"><i class="score">Pitchfork</i></a> --</cite>
